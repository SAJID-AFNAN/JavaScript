var color = "blue";
switch (color) {
    case 'green':
        console.log("You have to cross the road");
        break;
    case 'yellow':
        console.log("Wait a few minutes to cross the road");
        break;
    case 'red':
        console.log("Don't cross the road");
        break;
    default :
        console.log("At first go outside");
}